/**
 * Generates sdk/core/src/registry_data.rs from design-system-registry JSON files.
 * Run via: moon run sdk:codegen
 */

import { readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "../..");
const registryDir = join(repoRoot, "packages/design-system-registry/registry");
const destFile = join(__dirname, "../core/src/registry_data.rs");

const REGISTRIES = [
  ["components", "COMPONENTS_JSON"],
  ["states", "STATES_JSON"],
  ["variants", "VARIANTS_JSON"],
  ["sizes", "SIZES_JSON"],
  ["anatomy-terms", "ANATOMY_TERMS_JSON"],
  ["token-objects", "TOKEN_OBJECTS_JSON"],
  ["structures", "STRUCTURES_JSON"],
  ["substructures", "SUBSTRUCTURES_JSON"],
  ["orientations", "ORIENTATIONS_JSON"],
  ["positions", "POSITIONS_JSON"],
  ["densities", "DENSITIES_JSON"],
  ["shapes", "SHAPES_JSON"],
];

let generated =
  "// @generated by scripts/generate-registry-data.js from packages/design-system-registry/registry/*.json\n" +
  "// Do not edit manually — run `moon run sdk:codegen` to regenerate.\n\n";

for (const [stem, constName] of REGISTRIES) {
  const content = readFileSync(join(registryDir, `${stem}.json`), "utf-8");
  generated += `const ${constName}: &str = r##"${content}"##;\n`;
}

writeFileSync(destFile, generated, "utf-8");
console.log(`Written: ${destFile}`);
