/// Errors that can be returned from [`Dataset`] operations.
#[derive(Debug, thiserror::Error, uniffi::Error)]
pub enum DataError {
    /// The embedded cache blob was not compiled in (build without `--features embedded`).
    #[error("Embedded cache not available — build with `--features embedded` and run `moon run sdk-swift:cache-build`")]
    EmbeddedNotAvailable,

    /// The embedded cache blob could not be opened.
    #[error("Failed to open embedded cache: {message}")]
    CacheOpen { message: String },

    /// The supplied token JSON could not be parsed.
    #[error("Failed to parse token JSON: {message}")]
    ParseError { message: String },

    /// A query expression was malformed.
    #[error("Invalid query expression: {message}")]
    QueryError { message: String },

    /// An internal serialization error.
    #[error("Serialization error: {message}")]
    SerializeError { message: String },
}

