/// A single token record as returned by query results.
///
/// `raw` is the full source JSON of the token (all fields: name, value, uuid,
/// `$schema`, etc.) serialised as a JSON string for easy `Codable` decoding on
/// the Swift side.
#[derive(Debug, Clone, uniffi::Record)]
pub struct TokenResult {
    /// The canonical dot-path name of the token (e.g. `"color.accent.background.default.light"`).
    pub name: String,
    /// Full source JSON of the token — decode with `JSONDecoder` / `Codable`.
    pub raw: String,
    pub uuid: Option<String>,
    pub schema_url: Option<String>,
    pub alias_target: Option<String>,
    /// Layer this token belongs to (e.g. `"foundation"`, `"platform"`, `"component"`).
    pub layer: String,
}

/// A single resolved token result from [`Dataset::resolve`].
#[derive(Debug, Clone, uniffi::Record)]
pub struct ResolveResult {
    pub token: TokenResult,
    /// Cascade specificity score — higher means this token is a more precise match.
    pub specificity: u32,
}

/// The terminal value and traversal chain from [`Dataset::resolve_reference`].
#[derive(Debug, Clone, uniffi::Record)]
pub struct ReferenceChainResult {
    /// The resolved terminal value serialised as JSON (may be a string, number, or object).
    pub value: Option<String>,
    /// Ordered list: original reference → intermediate names → final value.
    pub chain: Vec<String>,
}

/// Severity level of a validation diagnostic.
#[derive(Debug, Clone, uniffi::Enum)]
pub enum Severity {
    Error,
    Warning,
    Info,
}

/// A single validation diagnostic.
#[derive(Debug, Clone, uniffi::Record)]
pub struct Diagnostic {
    pub severity: Severity,
    pub message: String,
    pub rule_id: Option<String>,
    /// Token path or instance path the diagnostic applies to.
    pub path: Option<String>,
}

/// The aggregate result of [`Dataset::validate`].
#[derive(Debug, Clone, uniffi::Record)]
pub struct ValidationResult {
    pub valid: bool,
    pub errors: Vec<Diagnostic>,
    pub warnings: Vec<Diagnostic>,
}

/// A key→value change within a token's properties.
#[derive(Debug, Clone, uniffi::Record)]
pub struct PropertyChange {
    pub path: String,
    /// One of `"added"`, `"removed"`, `"modified"`.
    pub change_type: String,
    /// New value as a JSON string (present when `change_type` is `"added"` or `"modified"`).
    pub new_value: Option<String>,
    /// Original value as a JSON string (present when `change_type` is `"removed"` or `"modified"`).
    pub original_value: Option<String>,
}

/// A token that was renamed (same UUID, different name).
#[derive(Debug, Clone, uniffi::Record)]
pub struct RenamedToken {
    pub old_name: String,
    pub new_name: String,
    pub uuid: Option<String>,
    pub property_changes: Vec<PropertyChange>,
}

/// A token that was added in the newer dataset.
#[derive(Debug, Clone, uniffi::Record)]
pub struct AddedToken {
    pub name: String,
    pub uuid: Option<String>,
}

/// A token that was removed from the newer dataset.
#[derive(Debug, Clone, uniffi::Record)]
pub struct DeletedToken {
    pub name: String,
    pub uuid: Option<String>,
}

/// A token that gained a `deprecated` marker.
#[derive(Debug, Clone, uniffi::Record)]
pub struct DeprecatedToken {
    pub name: String,
    pub uuid: Option<String>,
}

/// A token that had its `deprecated` marker removed.
#[derive(Debug, Clone, uniffi::Record)]
pub struct RevertedToken {
    pub name: String,
    pub uuid: Option<String>,
}

/// A token that had property-level changes (same name, same UUID).
#[derive(Debug, Clone, uniffi::Record)]
pub struct UpdatedToken {
    pub name: String,
    pub uuid: Option<String>,
    pub property_changes: Vec<PropertyChange>,
}

/// The result of [`Dataset::diff`].
#[derive(Debug, Clone, uniffi::Record)]
pub struct DiffResult {
    pub renamed: Vec<RenamedToken>,
    pub deprecated: Vec<DeprecatedToken>,
    pub reverted: Vec<RevertedToken>,
    pub added: Vec<AddedToken>,
    pub deleted: Vec<DeletedToken>,
    pub updated: Vec<UpdatedToken>,
}

/// A single ranked suggestion result from [`Dataset::suggest`].
#[derive(Debug, Clone, uniffi::Record)]
pub struct SuggestResult {
    pub token_uuid: Option<String>,
    pub token_name: String,
    pub file: String,
    pub layer: String,
    /// Jaccard similarity score in [0, 1].
    pub confidence: f32,
    /// Full name-object JSON (decode with `Codable` for structured access).
    pub name_object: Option<String>,
    /// Token value serialised as JSON.
    pub value: Option<String>,
}

// ---------------------------------------------------------------------------
// Conversion helpers
// ---------------------------------------------------------------------------

fn json_to_string(v: &serde_json::Value) -> String {
    v.to_string()
}

fn opt_json_to_string(v: &Option<serde_json::Value>) -> Option<String> {
    v.as_ref().map(json_to_string)
}

impl From<&design_data_core::graph::TokenRecord> for TokenResult {
    fn from(r: &design_data_core::graph::TokenRecord) -> Self {
        Self {
            name: r.name.clone(),
            raw: json_to_string(&r.raw),
            uuid: r.uuid.clone(),
            schema_url: r.schema_url.clone(),
            alias_target: r.alias_target.clone(),
            layer: format!("{:?}", r.layer).to_lowercase(),
        }
    }
}

impl From<&design_data_core::report::Severity> for Severity {
    fn from(s: &design_data_core::report::Severity) -> Self {
        match s {
            design_data_core::report::Severity::Error => Severity::Error,
            design_data_core::report::Severity::Warning => Severity::Warning,
            design_data_core::report::Severity::Info => Severity::Info,
        }
    }
}

impl From<&design_data_core::report::Diagnostic> for Diagnostic {
    fn from(d: &design_data_core::report::Diagnostic) -> Self {
        Self {
            severity: Severity::from(&d.severity),
            message: d.message.clone(),
            rule_id: d.rule_id.clone(),
            path: d.instance_path.clone().or_else(|| d.token.clone()),
        }
    }
}

impl From<design_data_core::report::ValidationReport> for ValidationResult {
    fn from(r: design_data_core::report::ValidationReport) -> Self {
        // NOTE: `ValidationReport.errors` holds ALL diagnostics regardless of severity
        // (there is no separate `warnings` field in core — see sdk/core/src/report.rs).
        // Split into two arrays by filtering the same slice twice.
        let errors = r
            .errors
            .iter()
            .filter(|d| matches!(d.severity, design_data_core::report::Severity::Error))
            .map(Diagnostic::from)
            .collect();
        let warnings = r
            .errors
            .iter()
            .filter(|d| {
                matches!(
                    d.severity,
                    design_data_core::report::Severity::Warning
                        | design_data_core::report::Severity::Info
                )
            })
            .map(Diagnostic::from)
            .collect();
        Self {
            valid: r.valid,
            errors,
            warnings,
        }
    }
}

impl From<design_data_core::suggest::SuggestionResult> for SuggestResult {
    fn from(r: design_data_core::suggest::SuggestionResult) -> Self {
        Self {
            token_uuid: r.token_uuid,
            token_name: r.token_name,
            file: r.file.to_string_lossy().into_owned(),
            layer: format!("{:?}", r.layer).to_lowercase(),
            confidence: r.confidence,
            name_object: opt_json_to_string(&r.name_object),
            value: opt_json_to_string(&r.value),
        }
    }
}

fn map_property_change(c: &design_data_core::diff::PropertyChange) -> PropertyChange {
    PropertyChange {
        path: c.path.clone(),
        change_type: format!("{:?}", c.change_type).to_lowercase(),
        new_value: opt_json_to_string(&c.new_value),
        original_value: opt_json_to_string(&c.original_value),
    }
}

