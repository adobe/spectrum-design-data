/// How a `Dataset` was constructed — determines the `primer` provenance field.
enum DatasetSource {
    /// Loaded from the embedded `.redb` blob compiled into the binary.
    ///
    /// Only reachable when the `embedded` feature is enabled.
    #[cfg_attr(not(feature = "embedded"), allow(dead_code))]
    Embedded,
    /// Constructed in-memory from caller-supplied token JSON.
    InMemory,
}

/// A loaded Spectrum token dataset you can query, validate, resolve, and diff.
///
/// # Usage (Swift)
///
/// ```swift
/// import DesignData
///
/// // Use the canonical embedded Spectrum dataset (built with --features embedded):
/// let ds = try Dataset.embedded()
/// let hits = try ds.query(expr: "property=color,colorScheme=dark")
///
/// // Or build from your own token JSON:
/// let json = try String(contentsOf: url)
/// let ds2 = try Dataset.fromTokens(json: json)
/// ```
#[derive(uniffi::Object)]
pub struct Dataset {
    graph: TokenGraph,
    source: DatasetSource,
}

#[uniffi::export]
impl Dataset {
    // -----------------------------------------------------------------------
    // Constructors
    // -----------------------------------------------------------------------

    /// Open the canonical embedded Spectrum dataset from the prebuilt cache.
    ///
    /// The cache is opened once and reused; subsequent calls are cheap.
    ///
    /// Throws [`DataError::EmbeddedNotAvailable`] if built without
    /// `--features embedded`, or [`DataError::CacheOpen`] if the blob is corrupt.
    #[uniffi::constructor]
    pub fn embedded() -> Result<std::sync::Arc<Dataset>, DataError> {
        #[cfg(feature = "embedded")]
        {
            use design_data_core::cache;
            let graph = EMBEDDED_GRAPH
                .get_or_init(|| {
                    cache::load_from_bytes(EMBEDDED_CACHE_BYTES).map_err(|e| e.to_string())
                })
                .as_ref()
                .map_err(|msg| DataError::CacheOpen {
                    message: msg.clone(),
                })?
                .clone();
            Ok(std::sync::Arc::new(Dataset {
                graph,
                source: DatasetSource::Embedded,
            }))
        }
        #[cfg(not(feature = "embedded"))]
        {
            Err(DataError::EmbeddedNotAvailable)
        }
    }

    /// Build a dataset from a JSON string containing an array of raw token objects.
    ///
    /// Accepts the same format as the cascade token JSON files — an array of token
    /// objects each with a `name` object (e.g. `{ "name": { "property": "..." }, "value": ... }`).
    ///
    /// # Parameters
    /// - `json`: a JSON string containing an array of token objects.
    ///
    /// Throws [`DataError::ParseError`] if the JSON is malformed or not an array.
    #[uniffi::constructor(name = "fromTokens")]
    pub fn from_tokens(json: String) -> Result<std::sync::Arc<Dataset>, DataError> {
        let tokens: Vec<serde_json::Value> =
            serde_json::from_str(&json).map_err(|e| DataError::ParseError {
                message: e.to_string(),
            })?;

        let pairs: Vec<(String, std::path::PathBuf, serde_json::Value)> = tokens
            .into_iter()
            .enumerate()
            .map(|(i, raw)| {
                let key = raw
                    .get("uuid")
                    .and_then(|v| v.as_str())
                    .map(String::from)
                    .unwrap_or_else(|| format!("token-{i}"));
                (key, std::path::PathBuf::from("input.json"), raw)
            })
            .collect();

        let graph = TokenGraph::from_pairs(pairs);
        Ok(std::sync::Arc::new(Dataset {
            graph,
            source: DatasetSource::InMemory,
        }))
    }

    // -----------------------------------------------------------------------
    // Primer
    // -----------------------------------------------------------------------

    /// Emit a structural overview of the dataset for tooling / agent context.
    ///
    /// Returns a JSON string with keys: `specVersion`, `tokenCount`, `modeSets`,
    /// `components`, `taxonomyFields`, `manifest`, `provenance`.
    /// Decode with `JSONDecoder` / `Codable` on the Swift side.
    ///
    /// Throws [`DataError::SerializeError`] if serialization fails (unlikely).
    pub fn primer(&self) -> Result<String, DataError> {
        let provenance = match self.source {
            DatasetSource::Embedded => serde_json::json!({
                "source": "embedded",
                "tokensVersion": primer::EMBEDDED_DATA_VERSION,
            }),
            DatasetSource::InMemory => serde_json::json!({ "source": "in-memory" }),
        };
        let data = primer::build(&self.graph, provenance);
        serde_json::to_string(&data).map_err(|e| DataError::SerializeError {
            message: e.to_string(),
        })
    }

    // -----------------------------------------------------------------------
    // Query
    // -----------------------------------------------------------------------

    /// Filter tokens using a query expression.
    ///
    /// **Syntax:** `key=value` pairs separated by `,` (AND) or `|` (OR).
    /// Use `!=` for negation and `*` as a wildcard suffix or prefix.
    /// Valid keys: `property`, `component`, `variant`, `state`, `colorScheme`,
    /// `scale`, `contrast`, `uuid`, `$schema`.
    ///
    /// Pass an empty string to return all tokens.
    ///
    /// Throws [`DataError::QueryError`] if the expression is malformed.
    pub fn query(&self, expr: String) -> Result<Vec<TokenResult>, DataError> {
        let filter = query::parse(&expr).map_err(|e| DataError::QueryError {
            message: e.to_string(),
        })?;
        let records = query::filter(&self.graph, &filter);
        Ok(records.iter().map(|r| TokenResult::from(*r)).collect())
    }

    // -----------------------------------------------------------------------
    // Suggest
    // -----------------------------------------------------------------------

    /// Rank existing tokens by similarity to a natural-language `intent` string.
    ///
    /// Uses Jaccard similarity over the bag-of-words formed from each token's
    /// key segments, name-object field values, and description text.
    ///
    /// # Parameters
    /// - `intent`: natural-language description (e.g. `"primary button background"`).
    /// - `property_hint`: optionally constrain to tokens with a specific property.
    /// - `limit`: maximum number of results to return.
    pub fn suggest(
        &self,
        intent: String,
        property_hint: Option<String>,
        limit: u32,
    ) -> Vec<SuggestResult> {
        suggest::suggest(
            &self.graph,
            &intent,
            property_hint.as_deref(),
            limit as usize,
        )
        .into_iter()
        .map(SuggestResult::from)
        .collect()
    }

    // -----------------------------------------------------------------------
    // Validate
    // -----------------------------------------------------------------------

    /// Run relational validation rules over the dataset.
    ///
    /// Returns a [`ValidationResult`] with `valid`, `errors`, and `warnings`.
    /// This runs the same rules as `design-data validate` but without structural
    /// (JSON Schema) checks — those require filesystem access.  Build
    /// `design-data-core` with `--features schema-resolvers` and use the CLI for
    /// full structural validation.
    pub fn validate(&self) -> ValidationResult {
        let report = validate_relational(&self.graph, &std::collections::HashSet::new(), None);
        ValidationResult::from(report)
    }

    // -----------------------------------------------------------------------
    // Resolve
    // -----------------------------------------------------------------------

    /// Resolve the winning token for a given `property` in a specific `context`.
    ///
    /// The `context` map maps mode-set names to their active mode values, e.g.:
    /// `["colorScheme": "dark", "scale": "medium"]`.
    ///
    /// Returns the winning token record plus its specificity, or `nil` if no token
    /// matches the property + context combination.
    pub fn resolve(
        &self,
        property: String,
        context: HashMap<String, String>,
    ) -> Option<ResolveResult> {
        let mut ctx = ResolutionContext::new();
        for (k, v) in &context {
            ctx = ctx.with(k.clone(), v.clone());
        }
        resolve_property(&self.graph, &property, &ctx)
            .into_iter()
            .find(|c| c.is_winner)
            .map(|winner| ResolveResult {
                token: TokenResult::from(&winner.record),
                specificity: winner.specificity,
            })
    }

    /// Resolve a legacy token reference by slug in a given context.
    ///
    /// Returns the terminal value and the full alias chain, or `nil` if the
    /// reference cannot be resolved.
    ///
    /// Context keys mirror cascade name-object fields:
    /// - `"colorScheme"` → `"light"` | `"dark"` | `"wireframe"`
    /// - `"scale"` → `"desktop"` | `"mobile"`
    #[uniffi::method(name = "resolveReference")]
    pub fn resolve_reference(
        &self,
        token_ref: String,
        context: HashMap<String, String>,
    ) -> Option<ReferenceChainResult> {
        resolve_reference(&self.graph, &token_ref, &context).map(|r| ReferenceChainResult {
            value: r.value.as_ref().map(json_to_string),
            chain: r.chain,
        })
    }

    // -----------------------------------------------------------------------
    // Diff
    // -----------------------------------------------------------------------

    /// Compute the semantic diff between this dataset and another.
    ///
    /// Returns a [`DiffResult`] with arrays of renamed, deprecated, reverted,
    /// added, deleted, and updated tokens.
    pub fn diff(&self, other: std::sync::Arc<Dataset>) -> DiffResult {
        let report = semantic_diff(&self.graph, &other.graph);

        DiffResult {
            renamed: report
                .renamed
                .iter()
                .map(|r| RenamedToken {
                    old_name: r.old_name.clone(),
                    new_name: r.new_name.clone(),
                    uuid: r.uuid.clone(),
                    property_changes: r.property_changes.iter().map(map_property_change).collect(),
                })
                .collect(),
            deprecated: report
                .deprecated
                .iter()
                .map(|r| DeprecatedToken {
                    name: r.name.clone(),
                    uuid: r.uuid.clone(),
                })
                .collect(),
            reverted: report
                .reverted
                .iter()
                .map(|r| RevertedToken {
                    name: r.name.clone(),
                    uuid: r.uuid.clone(),
                })
                .collect(),
            added: report
                .added
                .iter()
                .map(|r| AddedToken {
                    name: r.name.clone(),
                    uuid: r.uuid.clone(),
                })
                .collect(),
            deleted: report
                .deleted
                .iter()
                .map(|r| DeletedToken {
                    name: r.name.clone(),
                    uuid: r.uuid.clone(),
                })
                .collect(),
            updated: report
                .updated
                .iter()
                .map(|r| UpdatedToken {
                    name: r.name.clone(),
                    uuid: r.uuid.clone(),
                    property_changes: r.property_changes.iter().map(map_property_change).collect(),
                })
                .collect(),
        }
    }

    // -----------------------------------------------------------------------
    // Introspection
    // -----------------------------------------------------------------------

    /// Return the number of tokens in the dataset.
    #[uniffi::method(name = "tokenCount")]
    pub fn token_count(&self) -> u64 {
        self.graph.tokens.len() as u64
    }
}

