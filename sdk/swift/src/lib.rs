#[cfg(test)]
mod tests {
    use super::*;

    #[cfg(feature = "embedded")]
    #[test]
    fn embedded_loads_and_has_tokens() {
        let ds = Dataset::embedded().expect("embedded() should succeed");
        assert!(
            ds.token_count() > 0,
            "embedded dataset should have at least one token"
        );
    }

    #[cfg(feature = "embedded")]
    #[test]
    fn query_color_tokens() {
        let ds = Dataset::embedded().expect("embedded() should succeed");
        let results = ds
            .query("property=color".into())
            .expect("query should succeed");
        assert!(
            !results.is_empty(),
            "query for property=color should return results"
        );
        for r in &results {
            assert_eq!(r.layer.is_empty(), false, "layer should be set");
        }
    }

    #[cfg(feature = "embedded")]
    #[test]
    fn resolve_returns_winner() {
        let ds = Dataset::embedded().expect("embedded() should succeed");
        let ctx = [
            ("colorScheme".to_string(), "dark".to_string()),
            ("scale".to_string(), "desktop".to_string()),
        ]
        .into_iter()
        .collect();
        // "background" is a well-known property in Spectrum tokens.
        let result = ds.resolve("background".into(), ctx);
        // May be None if the property name changed — accept either outcome in a unit test.
        if let Some(r) = result {
            assert!(r.specificity > 0);
        }
    }

    #[test]
    fn from_tokens_roundtrip() {
        // Note: avoid '#' inside r# raw strings; use a plain color value instead.
        let json = "[{\"name\":{\"property\":\"color\"},\"value\":\"red\",\"uuid\":\"test-1\"}]";
        let ds = Dataset::from_tokens(json.to_string()).expect("from_tokens should succeed");
        assert_eq!(ds.token_count(), 1);
        let results = ds
            .query("property=color".into())
            .expect("query should succeed");
        assert_eq!(results.len(), 1);
    }

    #[test]
    fn from_tokens_rejects_bad_json() {
        let result = Dataset::from_tokens("not json".to_string());
        assert!(
            matches!(result, Err(DataError::ParseError { .. })),
            "malformed JSON should produce ParseError"
        );
    }
}
