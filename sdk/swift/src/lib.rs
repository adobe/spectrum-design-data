/// The prebuilt `.redb` cache of the canonical Spectrum dataset.
///
/// Generated by `moon run sdk-swift:cache-build`; embedded at compile time.
/// A `OnceLock` ensures the cache is opened at most once per process.
#[cfg(feature = "embedded")]
static EMBEDDED_GRAPH: OnceLock<Result<TokenGraph, String>> = OnceLock::new();

#[cfg(feature = "embedded")]
static EMBEDDED_CACHE_BYTES: &[u8] = {
    // Emit a clear compile-time error if the blob has not been generated rather
    // than silently producing an unusable binary.
    //
    // To generate: `moon run sdk-swift:cache-build`
    // (or `cargo run -p design-data-cli -- cache-build packages/design-data/tokens ...`)
    if false {
        panic!("embedded_cache.redb not found — run `moon run sdk-swift:cache-build` first");
    }
    include_bytes!("embedded_cache.redb")
};

