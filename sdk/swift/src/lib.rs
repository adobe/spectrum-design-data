//! Native Swift bindings for `design-data-core` via UniFFI.
//!
//! This crate exposes the full Spectrum design-data [`Dataset`] API —
//! query, resolve, validate, diff, suggest, and primer — as an idiomatic Swift
//! class, distributed as an XCFramework + Swift Package.
//!
//! # Data embedding
//!
//! When built with `--features embedded`, the canonical Spectrum token dataset
//! is baked into the binary as a prebuilt `.redb` cache blob and loaded
//! in-memory via [`design_data_core::cache::load_from_bytes`].  No filesystem
//! writes occur — the `.app` sandbox is fully respected.
//!
//! Build the blob first:
//! ```sh
//! moon run sdk-swift:cache-build
//! ```
//!
//! Then build the XCFramework:
//! ```sh
//! node sdk/swift/scripts/build-swift.mjs
//! ```
//!
//! # API surface
//!
//! All public items below are exported to Swift via the `#[uniffi::export]` and
//! `#[derive(uniffi::*)]` proc-macros. The generated `DesignDataSwift.swift` file
//! (emitted by `uniffi-bindgen generate --language swift`) is the primary Swift
//! integration point.

use std::collections::HashMap;
#[cfg(feature = "embedded")]
use std::sync::OnceLock;

use design_data_core::cascade::{resolve_property, resolve_reference, ResolutionContext};
use design_data_core::diff::semantic_diff;
use design_data_core::graph::TokenGraph;
use design_data_core::primer;
use design_data_core::query;
use design_data_core::suggest;
use design_data_core::validate::relational::validate_relational;

